export function buildHowToJsonLd({
  name,
  description,
  path,
  steps,
}: HowToOptions): Record<string, unknown> {
  const siteUrl = getSiteUrl();
  const url = `${siteUrl}${path}`;

  return {
    "@context": "https://schema.org",
    "@type": "HowTo",
    "@id": `${url}#howto`,
    name,
    description,
    url,
    inLanguage: "ru-RU",
    author: buildGuideAuthor(),
    step: steps.map((step, index) => ({
      "@type": "HowToStep",
      position: index + 1,
      name: step.name,
      text: step.text,
    })),
  };
}
